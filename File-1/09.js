// concatenation of string
let string1 = "ritish";
let string2 = "jaiswal";
console.log(string1 + " " + string2);
// it will print ritish jaiswal

let string3 = "10";
let string4 = "20";
let string5 = string3 + string4;
console.log(string5); // it will print 1020
console.log(typeof string5); //it will print string
let string6 = +string3 + +string4;
console.log(string6); // it will print 30
console.log(typeof string6); // it will print number
